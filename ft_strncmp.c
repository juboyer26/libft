#include "libft.h"

int     ft_strncmp(const char *s1, const char *s2, size_t n)
{
    int x;

    x = 0;
    while ( (size_t)x < n && (s1[x] != '\0') && (s1[x] == s2[x] ) )
        x++;
    return (s1[x] - s2[x]);
}
