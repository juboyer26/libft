#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char	*dst1;
	unsigned char	*src1;
	size_t			i;

	dst1 = (unsigned char *)dst;
	src1 = (unsigned char *)src;
	i = 0;
	while (src1[i] != '\0' && i < n)
	{
		dst1[i] = src1[i];
		i++;
	}
	dst1[i] = '\0';
	return (dst);
}
